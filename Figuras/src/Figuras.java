public class Figuras {
    float lado;
    int numLados;

    public Figuras(float lado, int numLados) {
        this.lado = lado;
        this.numLados = numLados;
    }

    public float getLado() {
        return lado;
    }

    public void setLado(float lado) {
        this.lado = lado;
    }

    public int getNumLados() {
        return numLados;
    }

    public void setNumLados(int numLados) {
        this.numLados = numLados;
    }
}
